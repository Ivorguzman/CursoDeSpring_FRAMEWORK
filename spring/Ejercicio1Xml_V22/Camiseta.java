package com.mitoCode.beans.Ejercicio1Xml_V22;

public class Camiseta {
	private int id;
	private int numero;
	private Marca nombre;



	public int getId() {
		return this.id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getNumero() {
		return this.numero;
	}

	public void setNumero(int numero) {
		this.numero = numero;
	}

	public Marca getNombre() {
		return this.nombre;
	}

	public void setNombre(Marca nombre) {
		this.nombre = nombre;
	}





}
